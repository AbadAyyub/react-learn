import logo from "./logo.svg";
import "./App.css";
import React, { Component } from "react";
import NavBar from "./components/navbar";
import Counters from "./components/counters";

class App extends Component {
  state = {
    counters: [
      { id: 1, value: 4 },
      { id: 2, value: 0 },
      { id: 3, value: 0 },
      { id: 4, value: 0 },
    ],
  };

  handleDelete = (counterId) => {
    console.log("Delete handler called", counterId);
    const counterUpdated = this.state.counters.filter(
      (c) => c.id !== counterId
    );
    this.setState({ counters: counterUpdated });
  };

  handleIncrement = (counter) => {
    console.log("Increment handler invoked");
    const counters = [...this.state.counters];
  };

  handleResest = () => {
    console.log("Reste handler invoked");
    const counters = this.state.counters.map((c) => {
      c.value = 0;
      return c;
    });

    this.setState({ counters });
  };

  render() {
    return (
      <React.Fragment>
        <NavBar
          totalCounters={this.state.counters.filter((c) => c.value > 0).length}
        />
        <main className="container">
          <Counters
            counters={this.state.counters}
            onReset={this.handleResest}
            onIncrement={this.handleIncrement}
            onDelete={this.handleDelete}
          />
        </main>
      </React.Fragment>
    );
  }
}

export default App;
